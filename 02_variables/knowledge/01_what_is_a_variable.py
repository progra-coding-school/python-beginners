# A variable is a name that stores a value.

name = "Aarav"
age = 13
marks = 92.5
is_passed = True

print(name)
print(age)
print(marks)
print(is_passed)

# Use variables in calculations
bonus = 5
total = marks + bonus
print("Original marks:", marks)
print("After bonus:", total)

# Use variables in print
print("Name:", name)
print("Age:", age)

# Good names describe what they hold:
# student_name, total_marks, is_passed   → clear
# x, y, z, a                            → confusing

