# Without Variables
print("Hello John")
print("John is 25 years old")
print("John's score: 95")

# With Variables

name = "John"
age = 25
score = 95
print(f"Hello { name }")
print(f"{ name } is { age } years old")
print(f"{ name }'s score: { score }")
