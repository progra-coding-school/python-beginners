# Variables can be changed anytime.

favourite_food = "Dosa"     # age 5
print(favourite_food)

favourite_food = "Pizza"    # age 10
print(favourite_food)

favourite_food = "Biryani"  # age 15
print(favourite_food)

# Each time, the variable holds only the latest value.

# Updating step by step
price = 100
print(price)       # 100

price = price + 20     # delivery charge
print(price)       # 120

price = price - 15     # discount
print(price)       # 105

# Shorthand operators
runs = 0
runs += 4    # boundary
runs += 6    # six
runs += 1    # single
print(runs)  # 11

# String reassignment
city = "Chennai"
print(city)

city = "Bengaluru"
print(city)

# String joining
message = "Hello"
message += ", Riya"
message += "! How are you?"
print(message)

