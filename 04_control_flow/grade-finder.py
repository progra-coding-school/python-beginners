score = 50
if score >= 90:
    print("Grade: A - Excellent!")
elif score >= 80 and score <90:
    print("Grade: B - Very Good!")
elif score >= 70 and score <80:
    print("Grade: C - Good.")
elif score >= 60 and score <70:
    print("Grade: D - Pass.")
else:
    print("Grade: F - Fail.")
print("Grading complete.")