# print() is a built-in Python function that displays information on the screen.
# Without print(), your program runs silently — no one sees what it did!

# --- Basic print ---
print("Hello, World!")

# --- print() can show text ---
print("My name is Aarav")
print("My school is Progra Kids Coding School")

# --- print() can show numbers ---
print(100)
print(3.14)

# --- print() can show the result of a calculation ---
print(10 + 5)     # shows 15
print(20 * 3)     # shows 60

# --- print() with nothing inside → prints a blank line ---
print()           # empty line — useful for spacing

# --- print() structure ---
# print ( "message" )
#   ↑         ↑
# function  argument (what to show)

# --- Key vocabulary ---
# function  → a command that does a specific job
# argument  → the value you give to the function inside ()
# string    → text inside quotes ("Hello" or 'Hello')
# output    → anything shown on the screen

# --- Multiple prints appear on separate lines ---
print("Line 1")
print("Line 2")
print("Line 3")

