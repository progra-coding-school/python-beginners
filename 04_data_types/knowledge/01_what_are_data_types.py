# Every value in Python has a TYPE that tells Python what kind of data it is.

# Integer (int) — whole numbers
age = 13
students_in_class = 35
print(age)
print(students_in_class)

# Float (float) — numbers with decimal points
height = 5.4
pi = 3.14159
print(height)
print(pi)

# String (str) — text inside quotes
name = "Aarav"
city = 'Chennai'
print(name)
print(city)

# Boolean (bool) — only True or False
is_passed = True
is_raining = False
print(is_passed)
print(is_raining)

# Check the type of any value using type()
print(type(age))        # <class 'int'>
print(type(height))     # <class 'float'>
print(type(name))       # <class 'str'>
print(type(is_passed))  # <class 'bool'>

