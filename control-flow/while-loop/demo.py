#While loop demo

count = 0
while count < 5:
     print(count)
     count += 1         # Increment count in each iteration
print("Loop finished!")


# Explanation:
# count starts at 0.
# The loop continues as long as count is less than 5.
# In each iteration, count is printed, and then incremented by 1.
# When count becomes 5, the condition count < 5 becomes False, and the
# loop terminates.