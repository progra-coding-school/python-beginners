# Title: Updating Items in a List

# You can change any item using its index

subjects = ["Maths", "Science", "English", "Hindi"]
print("Original list:", subjects)

# Change the item at index 2
subjects[2] = "Tamil"
print("After changing index 2:", subjects)

# Change the first item
subjects[0] = "Mathematics"
print("After changing index 0:", subjects)

# Change the last item
subjects[-1] = "Social Science"
print("After changing last item:", subjects)
