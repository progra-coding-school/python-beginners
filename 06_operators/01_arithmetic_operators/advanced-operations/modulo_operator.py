# Modulo (%) returns the remainder after division

print("Modulo Operator (%) Examples")

print(5 % 3)      # Output: 2
print(10 % 2)     # Output: 0
print(7 % 4)      # Output: 3

# Checking even or odd
num = 7
print(num % 2)    # 1 → Odd, 0 → Even
