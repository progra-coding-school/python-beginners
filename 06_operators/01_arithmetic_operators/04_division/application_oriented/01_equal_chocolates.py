# Title: Equal Chocolates
# Problem Statement:
# There are 12 chocolates.
# They are shared equally among 4 children.
# How many chocolates does each child get?
#
# Solution:
# Divide the total number of chocolates by the number of children.

total_chocolates = 12
children = 4

chocolates_per_child = total_chocolates // children

print("Chocolates per child:", chocolates_per_child)
