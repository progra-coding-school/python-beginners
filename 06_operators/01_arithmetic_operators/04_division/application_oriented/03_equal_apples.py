# Title: Equal Apples
# Problem Statement:
# There are 18 apples.
# They are shared equally among 6 friends.
# How many apples does each friend get?
#
# Solution:
# Divide the total number of apples by the number of friends.

total_apples = 18
friends = 6

apples_per_friend = total_apples // friends

print("Apples per friend:", apples_per_friend)
