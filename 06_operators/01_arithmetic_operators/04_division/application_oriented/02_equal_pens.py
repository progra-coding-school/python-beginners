# Title: Equal Pens
# Problem Statement:
# There are 20 pens.
# They are distributed equally among 5 students.
# How many pens does each student get?
#
# Solution:
# Divide the total number of pens by the number of students.

total_pens = 20
students = 5

pens_per_student = total_pens // students

print("Pens per student:", pens_per_student)
