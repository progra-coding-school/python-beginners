# Title: Study Time Left
# Problem Statement:
# A student planned to study for 3 hours.
# He already studied for 1 hour.
# How much study time is left?
#
# Solution:
# Subtract studied time from planned time.

planned_time = 3
studied_time = 1

time_left = planned_time - studied_time

print("Time left to study:", time_left, "hours")
