# Title: Age Difference
# Problem Statement:
# Aman is 10 years old and his sister Riya is 6 years old.
# Find the age difference between them.
#
# Solution:
# Subtract Riya's age from Aman's age to get the age difference.

aman_age = 10
riya_age = 6

age_difference = aman_age - riya_age

print("Age difference:", age_difference, "years")
