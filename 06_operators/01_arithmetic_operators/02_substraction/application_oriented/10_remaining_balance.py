# Title: Remaining Balance
# Problem Statement:
# Rohit had ₹500.
# He spent ₹120 on books and ₹80 on snacks.
# How much money is left?
#
# Solution:
# Subtract all expenses from the total money.

total_money = 500
books_cost = 120
snacks_cost = 80

remaining_money = total_money - books_cost - snacks_cost

print("Remaining money: ₹", remaining_money)
