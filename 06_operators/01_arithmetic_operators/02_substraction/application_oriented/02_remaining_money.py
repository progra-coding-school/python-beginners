# Title: Remaining Pocket Money
# Problem Statement:
# Rahul had ₹100.
# He spent ₹35 on snacks.
# How much money is left?
#
# Solution:
# Subtract the amount spent from the total money.

total_money = 100
spent_money = 35

remaining_money = total_money - spent_money

print("Remaining money: ₹", remaining_money)
