# Title: Distance Remaining
# Problem Statement:
# A child planned to walk 5 km.
# He has already walked 2 km.
# How much distance is left?
#
# Solution:
# Subtract walked distance from total distance.

total_distance = 5
walked_distance = 2

remaining_distance = total_distance - walked_distance

print("Remaining distance:", remaining_distance, "km")
