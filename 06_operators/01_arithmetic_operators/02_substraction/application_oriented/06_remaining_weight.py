# Title: Grocery Weight Left
# Problem Statement:
# A shopkeeper had 20 kg of rice.
# He sold 7 kg.
# How much rice is left?
#
# Solution:
# Subtract sold weight from total weight.

total_rice = 20
sold_rice = 7

remaining_rice = total_rice - sold_rice

print("Remaining rice:", remaining_rice, "kg")
