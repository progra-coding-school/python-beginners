# Title: Total Chocolates
# Problem Statement:
# There are 5 boxes.
# Each box contains 8 chocolates.
# Find the total number of chocolates.
#
# Solution:
# Multiply the number of boxes by the number of chocolates in each box.

boxes = 5
chocolates_per_box = 8

total_chocolates = boxes * chocolates_per_box

print("Total chocolates:", total_chocolates)
