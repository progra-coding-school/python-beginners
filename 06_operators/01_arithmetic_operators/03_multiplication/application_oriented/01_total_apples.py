# Title: Total Apples
# Problem Statement:
# There are 4 baskets.
# Each basket has 6 apples.
# Find the total number of apples.
#
# Solution:
# Multiply the number of baskets by the number of apples in each basket.

baskets = 4
apples_per_basket = 6

total_apples = baskets * apples_per_basket

print("Total apples:", total_apples)
