# An operator is a symbol that performs an action on values or variables.
# Python has different types of operators for different purposes.

# --- ARITHMETIC OPERATORS ---
# Used for math calculations
print("=== Arithmetic Operators ===")
print(10 + 3)    # Addition → 13
print(10 - 3)    # Subtraction → 7
print(10 * 3)    # Multiplication → 30
print(10 / 3)    # Division → 3.333...
print(10 // 3)   # Floor Division → 3 (no decimal)
print(10 % 3)    # Modulo → 1 (remainder)
print(10 ** 3)   # Exponentiation → 1000 (10³)

# --- COMPARISON OPERATORS ---
# Used to compare two values → always gives True or False
print("\n=== Comparison Operators ===")
print(10 == 10)  # Equal → True
print(10 != 5)   # Not equal → True
print(10 > 5)    # Greater than → True
print(10 < 5)    # Less than → False
print(10 >= 10)  # Greater or equal → True
print(10 <= 5)   # Less or equal → False

# --- LOGICAL OPERATORS ---
# Used to combine conditions → gives True or False
print("\n=== Logical Operators ===")
print(True and True)   # Both must be True → True
print(True and False)  # One is False → False
print(True or False)   # At least one True → True
print(False or False)  # Both False → False
print(not True)        # Flips it → False

