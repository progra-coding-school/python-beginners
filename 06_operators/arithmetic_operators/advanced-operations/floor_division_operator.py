# ===============================
# File Name: floor_division_operator.py
# Operator: // (Floor Division)
# ===============================

# // divides and removes the decimal part

print("Floor Division Operator (//) Examples")

print(5 // 3)     # Output: 1
print(10 // 3)    # Output: 3
print(20 // 4)    # Output: 5

# Floor division with negative numbers
print(-7 // 2)    # Output: -4
