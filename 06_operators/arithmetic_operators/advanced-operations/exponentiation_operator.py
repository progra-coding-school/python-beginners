# Operator: ** (Exponentiation)
# ===============================

# ** raises a number to the power of another

print("Exponentiation Operator (**) Examples")

print(5 ** 3)     # Output: 125
print(2 ** 4)     # Output: 16
print(10 ** 2)    # Output: 100
print(4 ** 0)     # Output: 1

# Square and cube
x = 6
print(x ** 2)     # Square → 36
print(x ** 3)     # Cube → 216
