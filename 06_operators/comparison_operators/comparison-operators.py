
print("COMPARISON OPERATORS Demo")

x=100
y=200

# 1.Equality Operator
print(x==y)     # Check the RHS and LHS are equal and return True or False

#2. Not Equal Operator
print(x!=y)     # Check the RHS and LHS are not equal and return True or False

#3. Greater than Operator
print(x>y)     # Check the LHS greater than RHS  and return True or False

#4. Lesser than Operator
print(x<y)     # Check the LHS lesser than RHS  and return True or False

#5. Greater than or Equal Operator
print(x>=y)     # Check the LHS greater than RHS or both equal and return True or false

#6. Lesser than or Equal  Operator
print(x<=y)     # Check the LHS lesser than RHS  or both equal and return True or false

