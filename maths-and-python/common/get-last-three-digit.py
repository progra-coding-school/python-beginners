# When you divide any whole number by 1000,
# the remainder (% 1000) is always the last 3 digit.
# Divide by 1000 → the leftover is the last 3 digit
#Ex:479 % 1000  → 479   # hundreds (last  three digits)

last_three_digit=479%1000
print(last_three_digit)

last_three_digit=1986%1000
print(last_three_digit)