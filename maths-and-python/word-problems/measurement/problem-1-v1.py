'''
A ribbon is 24.6 meters long.
It is cut into 6 equal pieces.
What is the length of each piece?
'''

# Length of the ribbon in meters
ribbon_length = 24.6

# Number of equal pieces
pieces = 6

# Calculate the length of each piece
length_of_each_piece = ribbon_length / pieces

# Display the result
print("Length of each piece:", length_of_each_piece, "meters")