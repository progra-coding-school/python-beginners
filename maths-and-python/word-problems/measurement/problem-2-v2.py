'''
Riya had some savings.
After dividing her total savings by 10, she got ₹18.5.
How much money did she save originally?
'''

# Get the savings amount after dividing by 10
savings_after_dividing = float(input("Enter the savings after dividing by 10: "))

# Calculate the original savings
original_savings = savings_after_dividing * 10

# Display the result
print("Riya's original savings = ₹", original_savings)